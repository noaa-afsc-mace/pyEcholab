"""An example of reading a sounder detected bottom .xyz file.

This script is an echolab2 example demonstrating how to read .xyz files
generated by the EK80 system. .xyz files store bottom detection data
by channel ID.

If you are simply looking to read data, transform it to Sv, Sp, or
power, and work with the sounder detected bottom line, you should use the
echosounder module's get_* methods. These methods will return processed
data with the bottom line included as the "bottom_line" attribute.

This example is intended to demonstrate how to do things the hard way.
(But not the *really* hard way. It still leverages the echosounder module
to read the raw and xyz data files.)


Definitions:

  The following attribute names are used in this example and they are
  defined here for clarity.

    transducer_offset is the vertical position of the transducer WITHOUT
    heave applied. It is a combination of the transducer_z_offset and the
    transducer_depth (EK60) or drop_keel_offset or water_level_draft (EK80)

    heave is the dynamic vertical displacement of your sampling platform
    provided by your platform's AHRS/IMU and as recorded by the EK60/EK80.

    transducer_draft is the vertical position of the transducer WITH heave
    applied:

        transducer_draft = transducer_offset + heave


The key takeaways:

 1) Bottom data must be read *after* its corresponding raw data. Bottom
    data will be matched to an existing "ping". If that ping doesn't
    exist, the bottom data value will be discarded.

 2) Keep your vertical axes consistent. Bottom detections are always
    returned as depth with heave correction applied (if heave data were
    available at the time of recording.) You must adjust the bottom line
    accordingly depending on the vertical axis of your sample data and
    whether or not heave correction has been applied to it. This can be
    done using the transducer_draft attribute of the bottom detection
    lines and the transducer_offset attribute of your processed_data
    object.

 3) If you change any parameters in the calibration object you must
    pass it using the "calibration" keyword when "getting" data from
    a raw_data object. In no calibration is provided, those methods
    will use the parameters from the .raw file.

"""

from matplotlib.pyplot import figure, show
from echolab2.instruments import echosounder
from echolab2.processing import line
from echolab2.plotting.matplotlib import echogram



#  set the path to a xyz file to read
xyz_38_file = 'C:/EK Test Data/EK80/DY2308/cw/DY2308_EK80-D20230702-T181248-ES38-7 Serial No  324 - Narrow.XYZ'


#  Set the path to a raw file to read. This is used for the part of the example
#  where we use the echosounder module to read the raw data and the corresponding
#  bottom files. The bottom files must be colocated with the raw data.
rawfile = 'C:/EK Test Data/EK80/DY2308/cw/DY2308_EK80-D20230702-T181248.raw'



#  if you simply want to read an .xyz file and do not care about the sample
#  data, you can use the echolab2.processing.line class to read the data and
#  return a line object containing the data. This will include the lat/lon,
#  sounder detected bottom, and the transducer draft (with heave applied, if
#  applicable.)
#
#  it is important to remember that the depths stored in the .xyz file are
#  computed using the sound speed that was configured in the EK software when
#  the data were recorded and they will have heave correction applied if heave
#  was input into the system. When reading .xyz files directly like this, you
#  are responsible for correcting the depths if the actual sound speed was
#  different from the configured sound speed.
print('reading the XYZ file...')
bottom_line_38 = line.read_xyz(xyz_38_file)
print(bottom_line_38)
