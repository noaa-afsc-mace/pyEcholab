#  most users would call one of the echosounder_data.get_* methods to get a processed
#  data object that contains the transformed sample data as well as bottom line. In this
#  example we'll do this all the hard way.

#  get the raw data for the 38 and 120 channels - we know that we have read a single EK80
#  file that contains data of a single data type so we know there will only be one
#  raw_data object in the list returned by get_channel_data() so we'll unpack that here.
#  If your example file contains multiple channels at 38 or 120 kHz or you read multiple
#  files containing different data types (complex, power+angle, power only, angle only)
#  there will be multiple raw_data objects per frequency and you will need to handle
#  that yourself.
raw_data = echosounder_data.get_channel_data(frequencies=[38000, 120000])
raw_data_38 = raw_data[38000][0]
raw_data_120 = raw_data[120000][0]


#  Create calibration objects for each channel. Unless you yourself collected the
#  data you are working with, you shouldn't trust the calibration parameters contained
#  within the raw data file for quantitative work. But, since this is just an example,
#  we are going to ignore best practices and use the cal values contained within
#  the raw file. We call the raw_data.get_calibration() method to extract the cal
#  params from the raw data and return calibration objects
cal_38 = raw_data_38.get_calibration()
cal_120 = raw_data_120.get_calibration()
